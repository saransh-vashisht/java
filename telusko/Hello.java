class Hello{
    public static void main(String a[]){
        System.out.println("Hello World");
        // way to put the binary number
        int num1= 0b101;
        System.out.println(num1);
        //  we can also put the hexadecimal numbers
        int num2= 0x7E;
        System.out.println(num2);
        // we can use the underscore to separate the value to count the data
        int num3= 10_00_00_000;
        System.out.println(num3);
}
}