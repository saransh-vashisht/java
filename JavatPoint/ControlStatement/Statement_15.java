
// JAVA break statement 
// it is used to be executed at the time to get out of flow of the program 
// it is used only inside the for loop and the switch statement
// it breaks out of inner loop in the case of the nested loop


public class Statement_15 {
    public static void main(String[] args) {
        for(int i=0;i<=10;i++){
            System.out.println(i);
            if (i==6) {
                break;
                
            }
        }
    }
}
