// Java For Each Loop


public class JavaLoops_5 {
    public static void main(String[] args) {
        // For each loop example
        // for(Data_type variable:array_name){
        //     // code to be executed
        // }

        // Declaring an array 
        int arr[]={12,23,44,56,78};
        // Printing array using for each loop
        for(int v:arr){
            System.out.print(v+" ");
        }
    }
}
